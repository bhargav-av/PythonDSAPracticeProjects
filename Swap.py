# Swap the values of two variables.

# Accept two integers from keyboard.
num1 = input("Enter the first value: ")
num2 = input("Enter the second value: ")

# Before Swap
print("Before swap")
print(f"num1 : {num1}")
print(f"num2 : {num2}")

# Swap values
temp = num1
num1 = num2
num2 = temp

# After Swap
print("After swap")
print(f"num1 : {num1}")
print(f"num2 : {num2}")

